package Modelo;

/**
 *
 * @author Admin
 */
public class Resumen {
    private String fecha; //Fechas
    private String cantRes; // Q Resoluciones
    private String mes; // Mes
    private String totalAyudas; // Total Montos
    private String totalBeneficiarios; // Total Beneficiarios

    public Resumen() {
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getCantRes() {
        return cantRes;
    }

    public void setCantRes(String cantRes) {
        this.cantRes = cantRes;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getTotalAyudas() {
        return totalAyudas;
    }

    public void setTotalAyudas(String totalAyudas) {
        this.totalAyudas = totalAyudas;
    }

    public String getTotalBeneficiarios() {
        return totalBeneficiarios;
    }

    public void setTotalBeneficiarios(String totalBeneficiarios) {
        this.totalBeneficiarios = totalBeneficiarios;
    }

 
    
}
